function getStoryById(id) {
  return HM_STORIES.find(s => s.id === id);
}

function getStoriesBySection(sectionId) {
  switch(sectionId) {
    case 'archivo': return HM_STORIES;
    case 'lecturas': return HM_STORIES.filter(s => s.terror >= 7);
    case 'potm': return HM_STORIES.filter(s => s.potm);
    case 'destacadas': return HM_STORIES.filter(s => s.featured);
    case 'concursos': return HM_STORIES.slice(0, 3);
    case 'usuarios': return HM_STORIES.filter(s => s.categories.includes('internet'));
    case 'memes': return [];
    case 'trollpasta': return [];
    default: return HM_STORIES;
  }
}

function getStoriesByTag(tag) {
  return HM_STORIES.filter(s => s.tags.includes(tag) || s.categories.includes(tag));
}

function searchStories(query) {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return HM_STORIES.filter(s =>
    s.title.toLowerCase().includes(q) ||
    s.author.toLowerCase().includes(q) ||
    s.excerpt.toLowerCase().includes(q) ||
    s.categories.some(c => c.toLowerCase().includes(q)) ||
    s.tags.some(t => t.toLowerCase().includes(q))
  );
}

function getTerrorLabel(terror) {
  if (terror >= 9) return 'PELIGRO EXTREMO';
  if (terror >= 7) return 'ALTO';
  if (terror >= 5) return 'MODERADO';
  if (terror >= 3) return 'BAJO';
  return 'MÍNIMO';
}

function getTerrorColor(terror) {
  if (terror >= 9) return 'var(--blood-arterial)';
  if (terror >= 7) return 'var(--blood-bright)';
  if (terror >= 5) return 'var(--rune-glow)';
  if (terror >= 3) return 'var(--blood-fresh)';
  return 'var(--paper-burnt)';
}
